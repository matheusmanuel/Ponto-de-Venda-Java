    public void cadastrar() {

        String sql = "INSERT into usuarios(nome,numero,email,usuario,senha,perfil) VALUES (?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtnome.getText());
            pst.setString(2, txtnumero.getText());
            pst.setString(3, txtemail.getText());
            pst.setString(4, txtusuario.getText());
            pst.setString(5, txtsenha.getText());
            pst.setString(6, perfilJ.getSelectedItem().toString());

            if ((txtnome.getText().isEmpty()) || (txtnumero.getText().isEmpty()) || (txtemail.getText().isEmpty()) || (txtusuario.getText().isEmpty()) || (txtsenha.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos Para Continuar");
            } else {
                int adicionar = pst.executeUpdate();
                if (adicionar > 0) {
                    JOptionPane.showMessageDialog(null,  txtnome.getText() +" Foi adicionado com sucesso!");
                    txtnome.setText(null);
                    txtnumero.setText(null);
                    txtemail.setText(null);
                    txtusuario.setText(null);
                    txtsenha.setText(null);
                    perfilJ.getSelectedIndex();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO: " + e.getMessage());
        }
    }

